package stage;

/**
 * ゲーム内のオブジェクトのレンダリング順序を定義する列挙型。
 * 列挙値の順序は描画レイヤーの優先順位を表し、下位のものから順に描画される。
 */
public enum RenderLayer {
	/**
	 * 床レイヤー。最も下層で描画される背景要素。
	 */
	FLOUR,

	/**
	 * 破片レイヤー。床の上に描画される破片やパーティクル。
	 */
	DEBRIS,

	/**
	 * 発射物レイヤー。弾丸やミサイルなどの発射物。
	 */
	PROJECTILE,

	/**
	 * 実体オブジェクトレイヤー。戦車や基地などの物理的な存在。
	 */
	TANGIBLE_OBJECT,
}
